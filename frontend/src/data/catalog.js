// Funciones de utilidad para el catálogo
export const getProductsByCategory = (categoryId) => {
  return catalogData.productos.filter(p => p.categoria === categoryId && p.estado === 'activo');
};

export const getProductById = (productId) => {
  return catalogData.productos.find(p => p.id === productId);
};

export const getFeaturedProducts = () => {
  return catalogData.productos.filter(p => p.destacado && p.estado === 'activo');
};

export const getPacks = () => {
  return catalogData.productos.filter(p => p.esPack && p.estado === 'activo');
};

export const formatPrice = (price) => {
  if (price === null || price === undefined) return 'Consultar';
  if (price === 0) return 'Incluido';
  return new Intl.NumberFormat('es-ES', {
    style: 'currency',
    currency: 'EUR',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0
  }).format(price);
};

export const getCategoryById = (categoryId) => {
  return catalogData.categorias.find(c => c.id === categoryId);
};

export default catalogData;
